package com.ensis.mediguru.models;

import java.util.ArrayList;

public class PatientOngoingMedicines {
	
	private ArrayList<GetPatientPastMedicines> ongoingMedicines=new ArrayList<GetPatientPastMedicines>();

	private ArrayList<GetPatientPastMedicines> pastMedicines =new ArrayList<GetPatientPastMedicines>();

	/**
	 * @return the ongoingMedicines
	 */
	public ArrayList<GetPatientPastMedicines> getOngoingMedicines() {
		return ongoingMedicines;
	}

	/**
	 * @param ongoingMedicines the ongoingMedicines to set
	 */
	public void setOngoingMedicines(
			ArrayList<GetPatientPastMedicines> ongoingMedicines) {
		this.ongoingMedicines = ongoingMedicines;
	}

	/**
	 * @return the pastMedicines
	 */
	public ArrayList<GetPatientPastMedicines> getPastMedicines() {
		return pastMedicines;
	}

	/**
	 * @param pastMedicines the pastMedicines to set
	 */
	public void setPastMedicines(ArrayList<GetPatientPastMedicines> pastMedicines) {
		this.pastMedicines = pastMedicines;
	}	

}
