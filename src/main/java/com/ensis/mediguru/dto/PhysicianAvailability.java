package com.ensis.mediguru.dto;

import java.sql.Time;
import java.util.Date;

import javax.persistence.Column;
import javax.persistence.Entity;
import javax.persistence.Id;
import javax.persistence.Table;

@Entity
@Table(name = "physicianavailability")
public class PhysicianAvailability {

	@Id
	private int phyavailid;

	@Column(name = "physicianid")
	private int physicianid;

	@Column(name = "fromday")
	private String fromday;

	@Column(name = "today")
	private String today;

	@Column(name = "fromtime")
	private String fromtime;

	@Column(name = "totime")
	private String totime;

	@Column(name = "status")
	private int status;

	@Column(name = "createdby")
	private String createdby;

	@Column(name = "createddate")
	private Date createddate;

	@Column(name = "updatedby")
	private String updatedby;

	@Column(name = "updateddate")
	private Date updateddate;

	public PhysicianAvailability() {
		super();
		// TODO Auto-generated constructor stub
	}

	public int getPhyavailid() {
		return phyavailid;
	}

	public void setPhyavailid(int phyavailid) {
		this.phyavailid = phyavailid;
	}

	public int getPhysicianid() {
		return physicianid;
	}

	public void setPhysicianid(int physicianid) {
		this.physicianid = physicianid;
	}

	public String getFromday() {
		return fromday;
	}

	public void setFromday(String fromday) {
		this.fromday = fromday;
	}

	public String getToday() {
		return today;
	}

	public void setToday(String today) {
		this.today = today;
	}

	



	/**
	 * @return the fromtime
	 */
	public String getFromtime() {
		return fromtime;
	}

	/**
	 * @param fromtime the fromtime to set
	 */
	public void setFromtime(String fromtime) {
		this.fromtime = fromtime;
	}

	/**
	 * @return the totime
	 */
	public String getTotime() {
		return totime;
	}

	/**
	 * @param totime the totime to set
	 */
	public void setTotime(String totime) {
		this.totime = totime;
	}

	public int getStatus() {
		return status;
	}

	public void setStatus(int status) {
		this.status = status;
	}

	public String getCreatedby() {
		return createdby;
	}

	public void setCreatedby(String createdby) {
		this.createdby = createdby;
	}

	public Date getCreateddate() {
		return createddate;
	}

	public void setCreateddate(Date createddate) {
		this.createddate = createddate;
	}

	public String getUpdatedby() {
		return updatedby;
	}

	public void setUpdatedby(String updatedby) {
		this.updatedby = updatedby;
	}

	public Date getUpdateddate() {
		return updateddate;
	}

	public void setUpdateddate(Date updateddate) {
		this.updateddate = updateddate;
	}

}
