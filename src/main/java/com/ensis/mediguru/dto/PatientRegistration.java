package com.ensis.mediguru.dto;

import java.util.Date;

import javax.persistence.Column;
import javax.persistence.Entity;
import javax.persistence.Id;
import javax.persistence.Table;

@Entity
@Table(name = "patient")
public class PatientRegistration {
	@Id
	private int patientid;
	@Column(name = "userid")
	private int userid;
	@Column(name = "firstname")
	private String firstname;
	@Column(name = "lastname")
	private String lastname;
	@Column(name = "dob")
	private String dob;
	@Column(name = "mobile")
	private String mobile;
	@Column(name = "skypeid")
	private String skypeid;
	@Column(name = "officephone")
	private String officephone;
	@Column(name = "contacthome")
	private String contacthome;
	@Column(name = "address1")
	private String address1;
	@Column(name = "address2")
	private String address2;
	@Column(name = "city")
	private String city;
	@Column(name = "stateid")
	private int stateid;
	@Column(name = "cid")
	private int cid;
	@Column(name = "zip")
	private String zip;
	@Column(name = "primaryregnumber")
	private String primaryregnumber;
	@Column(name = "about")
	private String about;
	@Column(name = "imgid")
	private int imgid;
	@Column(name = "createdby")
	private String createdby;
	@Column(name = "createddate")
	private Date createddate;
	@Column(name = "updatedby")
	private String updatedby;
	@Column(name = "updateddate")
	private Date updateddate;

	public PatientRegistration() {
		super();
		// TODO Auto-generated constructor stub
	}

	public PatientRegistration(int userid, String firstname, String lastname) {
		super();

		this.userid = userid;
		this.firstname = firstname;
		this.lastname = lastname;

	}

	public PatientRegistration(int patientid, int userid, String firstname,
			String lastname, String dob, String mobile, String skypeid,
			String officephone, String contacthome, String address1,
			String address2, String city, int stateid, int cid, String zip,
			String primaryregnumber, String about, int imgid) {
		super();
		this.patientid = patientid;
		this.userid = userid;
		this.firstname = firstname;
		this.lastname = lastname;
		this.dob = dob;
		this.mobile = mobile;
		this.skypeid = skypeid;
		this.officephone = officephone;
		this.contacthome = contacthome;
		this.address1 = address1;
		this.address2 = address2;
		this.city = city;
		this.stateid = stateid;
		this.cid = cid;
		this.zip = zip;
		this.primaryregnumber = primaryregnumber;
		this.about = about;
		this.imgid = imgid;
	}

	public int getPatientid() {
		return patientid;
	}

	public void setPatientid(int patientid) {
		this.patientid = patientid;
	}

	public int getUserid() {
		return userid;
	}

	public void setUserid(int userid) {
		this.userid = userid;
	}

	public String getFirstname() {
		return firstname;
	}

	public void setFirstname(String firstname) {
		this.firstname = firstname;
	}

	public String getLastname() {
		return lastname;
	}

	public void setLastname(String lastname) {
		this.lastname = lastname;
	}

	public String getDob() {
		return dob;
	}

	public void setDob(String dob) {
		this.dob = dob;
	}

	public String getMobile() {
		return mobile;
	}

	public void setMobile(String mobile) {
		this.mobile = mobile;
	}

	public String getSkypeid() {
		return skypeid;
	}

	public void setSkypeid(String skypeid) {
		this.skypeid = skypeid;
	}

	public String getOfficephone() {
		return officephone;
	}

	public void setOfficephone(String officephone) {
		this.officephone = officephone;
	}

	public String getAddress1() {
		return address1;
	}

	public void setAddress1(String address1) {
		this.address1 = address1;
	}

	public String getAddress2() {
		return address2;
	}

	public void setAddress2(String address2) {
		this.address2 = address2;
	}

	public String getCity() {
		return city;
	}

	public void setCity(String city) {
		this.city = city;
	}

	public int getStateid() {
		return stateid;
	}

	public void setStateid(int stateid) {
		this.stateid = stateid;
	}

	public int getCid() {
		return cid;
	}

	public void setCid(int cid) {
		this.cid = cid;
	}

	public String getZip() {
		return zip;
	}

	public void setZip(String zip) {
		this.zip = zip;
	}

	public String getPrimaryregnumber() {
		return primaryregnumber;
	}

	public void setPrimaryregnumber(String primaryregnumber) {
		this.primaryregnumber = primaryregnumber;
	}

	public String getAbout() {
		return about;
	}

	public void setAbout(String about) {
		this.about = about;
	}

	public int getImgid() {
		return imgid;
	}

	public void setImgid(int imgid) {
		this.imgid = imgid;
	}

	public String getCreatedby() {
		return createdby;
	}

	public void setCreatedby(String createdby) {
		this.createdby = createdby;
	}

	public Date getCreateddate() {
		return createddate;
	}

	public void setCreateddate(Date createddate) {
		this.createddate = createddate;
	}

	public String getUpdatedby() {
		return updatedby;
	}

	public void setUpdatedby(String updatedby) {
		this.updatedby = updatedby;
	}

	public Date getUpdateddate() {
		return updateddate;
	}

	public void setUpdateddate(Date updateddate) {
		this.updateddate = updateddate;
	}

	public String getContacthome() {
		return contacthome;
	}

	public void setContacthome(String contacthome) {
		this.contacthome = contacthome;
	}

}
