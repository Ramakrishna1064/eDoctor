package com.ensis.mediguru.dto;

import java.util.Date;

import javax.persistence.Entity;
import javax.persistence.Id;
import javax.persistence.Table;

/**
 * @author shyam
 *
 */
@Entity
@Table(name = "physiciandeletedquestions")
public class PhysicianDeletedQuestionsDTO {

	@Id
	private int deletedquesid;
	private int treatmentquestionid;
	private int physicianid;
	private String createdby;
	private Date createddate;

	public PhysicianDeletedQuestionsDTO() {
		super();
	}

	/**
	 * @return the deletedquesid
	 */
	public int getDeletedquesid() {
		return deletedquesid;
	}

	/**
	 * @param deletedquesid
	 *            the deletedquesid to set
	 */
	public void setDeletedquesid(int deletedquesid) {
		this.deletedquesid = deletedquesid;
	}

	/**
	 * @return the treatmentquestionid
	 */
	public int getTreatmentquestionid() {
		return treatmentquestionid;
	}

	/**
	 * @param treatmentquestionid
	 *            the treatmentquestionid to set
	 */
	public void setTreatmentquestionid(int treatmentquestionid) {
		this.treatmentquestionid = treatmentquestionid;
	}

	/**
	 * @return the physicianid
	 */
	public int getPhysicianid() {
		return physicianid;
	}

	/**
	 * @param physicianid
	 *            the physicianid to set
	 */
	public void setPhysicianid(int physicianid) {
		this.physicianid = physicianid;
	}

	/**
	 * @return the createdby
	 */
	public String getCreatedby() {
		return createdby;
	}

	/**
	 * @param createdby
	 *            the createdby to set
	 */
	public void setCreatedby(String createdby) {
		this.createdby = createdby;
	}

	/**
	 * @return the createddate
	 */
	public Date getCreateddate() {
		return createddate;
	}

	/**
	 * @param createddate
	 *            the createddate to set
	 */
	public void setCreateddate(Date createddate) {
		this.createddate = createddate;
	}

}
