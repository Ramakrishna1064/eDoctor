package com.ensis.mediguru.dto;

import java.util.Date;

import javax.persistence.Column;
import javax.persistence.Entity;
import javax.persistence.Id;
import javax.persistence.Table;

@Entity
@Table(name="treatmentquestion")
public class TreatmentQuestion {
	
	@Id
	private int treatmentquestionid;
	
	@Column(name="patientid")
	private int patientid;
	
	@Column(name="questiontypeid")
	private int questiontypeid;
	
	@Column(name="treatmentid")
	private int treatmentid;
	
	@Column(name="Question")
	private String question;
	
	@Column(name="description")
	private String description;
	
	@Column(name="imageid")
	private int imageid;
	
	@Column(name="comment")
	private String comment;
	
	@Column(name="rating")
	private int  rating;
	
	@Column(name="createdby")
	private String  createdby;
	
	@Column(name="createddate")
	private Date  createddate;
	
	@Column(name="updatedby")
	private String  updatedby;
	
	@Column(name="updateddate")
	private Date  updateddate;
	
	
	
	
	
	public TreatmentQuestion() {
		super();
		// TODO Auto-generated constructor stub
	}





	public TreatmentQuestion(int treatmentquestionid, int patientid,
			int questiontypeid, int treatmentid, String question,
			String description, int imageid, String comment, int rating,
			String createdby, Date createddate, String updatedby,
			Date updateddate) {
		super();
		this.treatmentquestionid = treatmentquestionid;
		this.patientid = patientid;
		this.questiontypeid = questiontypeid;
		this.treatmentid = treatmentid;
		this.question = question;
		this.description = description;
		this.imageid = imageid;
		this.comment = comment;
		this.rating = rating;
		this.createdby = createdby;
		this.createddate = createddate;
		this.updatedby = updatedby;
		this.updateddate = updateddate;
	}





	public int getTreatmentquestionid() {
		return treatmentquestionid;
	}





	public void setTreatmentquestionid(int treatmentquestionid) {
		this.treatmentquestionid = treatmentquestionid;
	}





	public int getPatientid() {
		return patientid;
	}





	public void setPatientid(int patientid) {
		this.patientid = patientid;
	}





	public int getQuestiontypeid() {
		return questiontypeid;
	}





	public void setQuestiontypeid(int questiontypeid) {
		this.questiontypeid = questiontypeid;
	}





	public int getTreatmentid() {
		return treatmentid;
	}





	public void setTreatmentid(int treatmentid) {
		this.treatmentid = treatmentid;
	}





	public String getQuestion() {
		return question;
	}





	public void setQuestion(String question) {
		this.question = question;
	}





	public String getDescription() {
		return description;
	}





	public void setDescription(String description) {
		this.description = description;
	}





	public int getImageid() {
		return imageid;
	}





	public void setImageid(int imageid) {
		this.imageid = imageid;
	}





	public String getComment() {
		return comment;
	}





	public void setComment(String comment) {
		this.comment = comment;
	}





	public int getRating() {
		return rating;
	}





	public void setRating(int rating) {
		this.rating = rating;
	}





	public String getCreatedby() {
		return createdby;
	}





	public void setCreatedby(String createdby) {
		this.createdby = createdby;
	}





	public Date getCreateddate() {
		return createddate;
	}





	public void setCreateddate(Date createddate) {
		this.createddate = createddate;
	}





	public String getUpdatedby() {
		return updatedby;
	}





	public void setUpdatedby(String updatedby) {
		this.updatedby = updatedby;
	}





	public Date getUpdateddate() {
		return updateddate;
	}





	public void setUpdateddate(Date updateddate) {
		this.updateddate = updateddate;
	}





	
	
}
