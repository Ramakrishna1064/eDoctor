package com.ensis.mediguru.dto;

import java.util.Date;

import javax.persistence.Column;
import javax.persistence.Entity;
import javax.persistence.Id;
import javax.persistence.Table;

@Entity
@Table(name = "physicianfaclservice")
public class PhysicianFacilityService {
	@Id
	private int physicianfaclserviceid;

	@Column(name = "physicianfacilityid")
	private int physicianfacilityid;

	@Column(name = "servicename")
	private String servicename;

	@Column(name = "status")
	private int status;

	@Column(name = "createdby")
	private String createdby;

	@Column(name = "createddate")
	private Date createddate;

	@Column(name = "updatedby")
	private String updatedby;

	@Column(name = "updateddate")
	private Date updateddate;

	public PhysicianFacilityService() {
		super();
		// TODO Auto-generated constructor stub
	}

	public PhysicianFacilityService(int physicianfacilityid) {
		super();
		this.physicianfacilityid = physicianfacilityid;
	}

	public int getPhysicianfaclserviceid() {
		return physicianfaclserviceid;
	}

	public void setPhysicianfaclserviceid(int physicianfaclserviceid) {
		this.physicianfaclserviceid = physicianfaclserviceid;
	}

	public int getPhysicianfacilityid() {
		return physicianfacilityid;
	}

	public void setPhysicianfacilityid(int physicianfacilityid) {
		this.physicianfacilityid = physicianfacilityid;
	}

	public int getStatus() {
		return status;
	}

	public void setStatus(int status) {
		this.status = status;
	}

	public String getCreatedby() {
		return createdby;
	}

	public void setCreatedby(String createdby) {
		this.createdby = createdby;
	}

	public Date getCreateddate() {
		return createddate;
	}

	public void setCreateddate(Date createddate) {
		this.createddate = createddate;
	}

	public String getUpdatedby() {
		return updatedby;
	}

	public void setUpdatedby(String updatedby) {
		this.updatedby = updatedby;
	}

	public Date getUpdateddate() {
		return updateddate;
	}

	public void setUpdateddate(Date updateddate) {
		this.updateddate = updateddate;
	}

	public String getServicename() {
		return servicename;
	}

	public void setServicename(String servicename) {
		this.servicename = servicename;
	}
	

}
