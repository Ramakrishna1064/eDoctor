package com.ensis.mediguru.dto;

import java.util.Date;

import javax.persistence.Column;
import javax.persistence.Entity;
import javax.persistence.Id;
import javax.persistence.Table;

/**
 * @author ensis
 *
 */
@Entity
@Table(name = "treatmentimages")
public class TreatmentImages {

	@Id
	private int imageid;

	@Column(name = "treatmentquestionid")
	private int treatmentquestionid;

	@Column(name = "filename")
	private String filename;

	@Column(name = "image")
	private String image;

	@Column(name = "createdby")
	private String createdby;

	@Column(name = "createddate")
	private Date createddate;

	@Column(name = "updatedby")
	private String updatedby;

	@Column(name = "updateddate")
	private Date updateddate;

	private Integer physicianid;
	private int imgtype;
	private int filetype;
	private String thumbnail;	

	public String getThumbnail() {
		return thumbnail;
	}

	public void setThumbnail(String thumbnail) {
		this.thumbnail = thumbnail;
	}

	public int getFiletype() {
		return filetype;
	}

	public void setFiletype(int filetype) {
		this.filetype = filetype;
	}

	/**
	 * @return the imageid
	 */
	public int getImageid() {
		return imageid;
	}

	/**
	 * @param imageid
	 *            the imageid to set
	 */
	public void setImageid(int imageid) {
		this.imageid = imageid;
	}

	/**
	 * @return the treatmentquestionid
	 */
	public int getTreatmentquestionid() {
		return treatmentquestionid;
	}

	/**
	 * @param treatmentquestionid
	 *            the treatmentquestionid to set
	 */
	public void setTreatmentquestionid(int treatmentquestionid) {
		this.treatmentquestionid = treatmentquestionid;
	}

	/**
	 * @return the filename
	 */
	public String getFilename() {
		return filename;
	}

	/**
	 * @param filename
	 *            the filename to set
	 */
	public void setFilename(String filename) {
		this.filename = filename;
	}

	/**
	 * @return the image
	 */
	public String getImage() {
		return image;
	}

	/**
	 * @param image
	 *            the image to set
	 */
	public void setImage(String image) {
		this.image = image;
	}

	/**
	 * @return the createdby
	 */
	public String getCreatedby() {
		return createdby;
	}

	/**
	 * @param createdby
	 *            the createdby to set
	 */
	public void setCreatedby(String createdby) {
		this.createdby = createdby;
	}

	/**
	 * @return the createddate
	 */
	public Date getCreateddate() {
		return createddate;
	}

	/**
	 * @param createddate
	 *            the createddate to set
	 */
	public void setCreateddate(Date createddate) {
		this.createddate = createddate;
	}

	/**
	 * @return the updatedby
	 */
	public String getUpdatedby() {
		return updatedby;
	}

	/**
	 * @param updatedby
	 *            the updatedby to set
	 */
	public void setUpdatedby(String updatedby) {
		this.updatedby = updatedby;
	}

	/**
	 * @return the updateddate
	 */
	public Date getUpdateddate() {
		return updateddate;
	}

	/**
	 * @param updateddate
	 *            the updateddate to set
	 */
	public void setUpdateddate(Date updateddate) {
		this.updateddate = updateddate;
	}

	/**
	 * @return the physicianid
	 */
	public Integer getPhysicianid() {
		return physicianid;
	}

	/**
	 * @param physicianid
	 *            the physicianid to set
	 */
	public void setPhysicianid(Integer physicianid) {
		this.physicianid = physicianid;
	}

	/**
	 * @return the imgtype
	 */
	public int getImgtype() {
		return imgtype;
	}

	/**
	 * @param imgtype
	 *            the imgtype to set
	 */
	public void setImgtype(int imgtype) {
		this.imgtype = imgtype;
	}

}
